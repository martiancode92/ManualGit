    private void txtFechaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFechaKeyPressed
      
        switch(evt.getKeyCode()){
        
            case KeyEvent.VK_ENTER:
                
                    txtTitulo.requestFocus();;
                    
            case KeyEvent.VK_ESCAPE:
                
                    txtFecha.setText("");
                    
                    
                    
        
        
        }
        
    }//GEN-LAST:event_txtFechaKeyPressed

    private void txtTituloKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTituloKeyPressed
        
            switch(evt.getKeyCode()){
        
            case KeyEvent.VK_ENTER:
                
                    btnGrabar.requestFocus();;
                    
            case KeyEvent.VK_ESCAPE:
                
                    txtTitulo.setText("");
                    
            } 
    }//GEN-LAST:event_txtTituloKeyPressed
private void limpiar(){
    txtTitulo.setText("");
    txtFecha.setText("");
    cmbCategoria.setSelectedIndex(0);
}

private boolean comprobarBlancos( String texto){



return texto.trim().equalsIgnoreCase("")?true:false;
}

