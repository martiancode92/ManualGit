    private void btnValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidarActionPerformed

        String login = txtLogin.getText();
        String pass = new String(pwdContra.getPassword());
        // Creo un objeto de tipo Usuario con los datos que identifica de
        // forma única al usuario
        Usuario u = new Usuario(login, pass);
        // indexOf busca la primera posicion donde hay un usuario con ese
        // login y contraseña . En el caso de no encontrarla indexOf nos devuelve
        //-1
  
        int pos = miEmpresa.getUsuarios().indexOf(u);
        //  if( miEmpresa.getUsuarios().contains(u)){

        if (pos != -1) {
            // Búsqueda existosa
            Usuario usuario2 = miEmpresa.getUsuarios().get(pos);
            // Instancio un objeto de tipo empresa
            // en el cual le paso la emprsa y el usuario validado
            new Principal(miEmpresa,usuario2).setVisible(true);
            //dispose.Libera todos los recursos nativos de pantalla utilizados por esta ventana, 
            //sus subcomponentes y todos sus hijos. Es decir,
            //los recursos para estos componentes se destruirán,
            //cualquier memoria que consuman será devuelta al sistema operativo 
            dispose();
            //El método pack ()  se define en la clase Window  en Java 
            //y dimensiona el marco para que todo su contenido sea igual o superior a sus tamaños preferidos. 
            pack();
        } else {
            JOptionPane.showMessageDialog(this, " usuario Inexistente",
                    "Confirmacion", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnValidarActionPerformed

