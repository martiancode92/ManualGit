    private void cargarNoticias(){
        ArrayList<Noticia> noticias=miEmpresa.getNoticias();
        for( int pos=0; pos <noticias.size();pos++){
            Vector v=new Vector();
            v.add(noticias.get(pos).getTitulo());
            v.add(noticias.get(pos).getUsuario().getNombre());
            modelo.addRow(v);
                       
        }
    }

