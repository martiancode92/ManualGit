    private void tblNoticiaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNoticiaMouseClicked
     int fila=tblNoticia.rowAtPoint(evt.getPoint());
     int columna= tblNoticia.columnAtPoint(evt.getPoint());
     String titulo= (String)modelo.getValueAt(fila, columna);
     Noticia n= new Noticia(titulo);
     int pos=miEmpresa.getNoticias().indexOf(n);
     if(pos!=-1)
     {
         txtFecha.setText(miEmpresa.getNoticias().get(pos).getFecha());
         txtCategoria.setText(miEmpresa.getNoticias().get(pos).getCategoria().getDenominacion());
         txtTitulo.setText(titulo);
         if(miEmpresa.getNoticias().get(pos).getUsuario()==usuarioValidado)
         {
             btnEliminar.setEnabled(true);
         }
         else
         {
             btnEliminar.setEnabled(false);
         }
         
     }
    }//GEN-LAST:event_tblNoticiaMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       int fila= tblNoticia.getSelectedRow();
       String titulo= txtTitulo.getText().toString();
     Noticia n= new Noticia(titulo);
     int pos=miEmpresa.getNoticias().indexOf(n);
     miEmpresa.getNoticias().remove(pos);
     modelo.removeRow(fila);
    }//GEN-LAST:event_btnEliminarActionPerformed


