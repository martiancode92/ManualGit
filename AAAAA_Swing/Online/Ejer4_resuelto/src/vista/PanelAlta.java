    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        String titulo, fecha;
        Noticia noticia;
        Categoria categoria;
        int pos = cmbCategoria.getSelectedIndex();
        titulo = txtTitulo.getText();
        fecha = txtFecha.getText();
// COMBPROBAR QUE EL USUARIO HAYA SELECCIONADO UNA CATEGORIA
        if (pos == 0) {
// EL USUARIO NO HA SELECCIONADO CATEGORIA, POR LO QUE SE EMITE UN MENSAJE DE ERROR.
            JOptionPane.showMessageDialog(this, " Debe de seleccionar una categoria primero",
                    "Error", JOptionPane.ERROR_MESSAGE);
            cmbCategoria.requestFocus();
        } else {
            categoria = (Categoria) modelo.getElementAt(pos);
            // COMPROBAMOS QUE HA INTRODUCIDO INFORMACIÃ“N EN TODOS LOS CAMPOS REQUERIDOS
            if (comprobarBlancos(titulo)) {
                JOptionPane.showMessageDialog(this, "Campo requerido",
                        "Error", JOptionPane.ERROR_MESSAGE);
                txtTitulo.setText("");
                txtTitulo.requestFocus();
            } else {
                if (comprobarBlancos(fecha)) {
                    JOptionPane.showMessageDialog(this, "Campo requerido",
                            "Error", JOptionPane.ERROR_MESSAGE);
                    txtFecha.setText("");
                    txtFecha.requestFocus();
                } else {
                    // CREAMOS LA NOTICIA
                    noticia = new Noticia(titulo, categoria, usuarioValidado, fecha);
                    if (miEmpresa.nuevaNoticia(noticia)) {
                        // EL METODO NUEVANOTICA DE LA CLASE EMPRESA DEVUELVE TRUE CUANDO LA NOTICIA YA EXISTE
                        JOptionPane.showMessageDialog(this, "Ya existe una noticia con ese titulo",
                                "Error", JOptionPane.ERROR_MESSAGE);
                        txtTitulo.requestFocus();
                        txtTitulo.setSelectionStart(0);
                        txtTitulo.setSelectionEnd(txtTitulo.getText().length());
                    } else {
                        JOptionPane.showMessageDialog(this, "Nueva noticia incorporada",
                                "correcto", JOptionPane.INFORMATION_MESSAGE);
                        limpiar();
                    }
                }
            }

        }
    }//GEN-LAST:event_btnGrabarActionPerformed

