    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
       
        String cif, nombre, poblacion,direccion;
        boolean existe;
        if(!comprobarBlancos(txtCif)&& !comprobarBlancos(txtNombre)&& 
                !comprobarBlancos(txtDireccion)&& !comprobarBlancos(txtPoblacion)){
            
            cif= txtCif.getText();
            nombre=txtNombre.getText();
            poblacion=txtPoblacion.getText();
            direccion= txtDireccion.getText();
            
            existe=miEmpresa.nuevoCliente(cif, nombre, direccion, direccion);
            if(!existe){
            
                JOptionPane.showMessageDialog(this, "USUARIO-AÑADIDO","",JOptionPane.INFORMATION_MESSAGE);
            
            }else{
            
                JOptionPane.showMessageDialog(this, "USUARIO YA EXSITENTE","",JOptionPane.ERROR_MESSAGE);
            
            }
            
        
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

    
    public boolean comprobarBlancos(JTextField campo){
        
        boolean vacio= campo.getText().trim().isEmpty();
        
    if(vacio){
        
         JOptionPane.showMessageDialog(this, " Campo Requerido", " Validación ", JOptionPane.ERROR_MESSAGE);
            campo.requestFocus();
    
    
    }
        return vacio;
    }

