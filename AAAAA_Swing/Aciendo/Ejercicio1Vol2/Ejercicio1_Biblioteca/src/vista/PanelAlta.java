    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
       
        String nombre, login, pass;
        
        int resultado;
        
        if(!validarCampo(txtNombre) && !validarCampo(txtLogin) && !validarCampo(txtPass)){
        
        
            nombre= txtNombre.getText();
            login= txtLogin.getText();
            pass= txtPass.getText();
            
            resultado = miBiblio.nuevoUsuario(nombre, login, pass);
            
            if(resultado==-1){
            
            JOptionPane.showMessageDialog(this,"Usuario A침adido","Informacion",JOptionPane.INFORMATION_MESSAGE);
            
            
            }else{
            
            
                 JOptionPane.showMessageDialog(this, "Ya existe un cliente con ese nombre",
                        "Alta", JOptionPane.ERROR_MESSAGE);
                 
                  txtNombre.requestFocus();
                // selecciono el texto introducido
                txtNombre.setSelectionStart(0);// comienzo de la selecci칩n
                txtNombre.setSelectionEnd(txtNombre.getText().length());
            
            }
        
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

