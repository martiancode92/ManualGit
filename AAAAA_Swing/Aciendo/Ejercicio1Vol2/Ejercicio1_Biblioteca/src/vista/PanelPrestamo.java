    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        String nombre= txtNombre.getText().trim();
        Usuario u;
        Libro l;
        
        int existe=miBiblio.busqueda(nombre);//retorna un valor numerico
        
        if(existe==-1){// no existe el usuario
        
            
            JOptionPane.showMessageDialog(this, "usuario no exisite" ,"",JOptionPane.ERROR_MESSAGE);
            
                //Limpio el campo
            txtNombre.setText("");
            // Coloco el cursor en el campo
            txtNombre.requestFocus();
        
        }else{
        
            if(cmbLibro.getSelectedIndex() !=-1){
                
                
                l = (Libro) modelo.getElementAt(cmbLibro.getSelectedIndex());
            
            
                l.setPrestado(true);
                
                u = miBiblio.getUsuarios().get(existe);
                
                u.nuevoLibro(l);
                
                JOptionPane.showMessageDialog(this, " Préstamo realizado",
                    "Información",JOptionPane.INFORMATION_MESSAGE);
                limpiar();
            }
            else{
            
                 JOptionPane.showMessageDialog(this, " No hay libros para prestar", 
                        "Información", JOptionPane.INFORMATION_MESSAGE);
            
            }
        
        
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed


