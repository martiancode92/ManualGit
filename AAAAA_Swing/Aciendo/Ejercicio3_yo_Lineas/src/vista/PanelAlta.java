    private void txtFrecuenciaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtFrecuenciaFocusLost

        String frecuencia = txtFrecuencia.getText();

        try {

            Integer.parseInt(frecuencia);

        } catch (NumberFormatException ex) {

            JOptionPane.showMessageDialog(this, "Deben ser Numeros");
            txtFrecuencia.requestFocus();
            txtFrecuencia.setSelectionStart(0);
            txtFrecuencia.setSelectionEnd(frecuencia.length());

        }
    }//GEN-LAST:event_txtFrecuenciaFocusLost

    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed

        int codigo, frecuencia;
        String denominacion;
        Parada primera, ultima;
        ArrayList<Parada> intermedia = new ArrayList();
        Linea l;

        codigo = Integer.parseInt(txtCodigo.getText());
        l = new Linea(codigo);

        if (!miEmpresa.getLineas().contains(l)) {

            denominacion = txtDenominacion.getText();
            frecuencia = Integer.parseInt(txtFrecuencia.getText());
            primera = (Parada) modeloPrimera.getElementAt(cmbPrimera.getSelectedIndex());
            ultima = (Parada) modeloUltima.getElementAt(cmbUltima.getSelectedIndex());
            for (int i = 0; i < modeloInterFinal.size(); i++) {
                intermedia.add((Parada) modeloInterFinal.getElementAt(i));

            }
            l = new Linea(codigo, denominacion, frecuencia, primera, ultima, intermedia);
            miEmpresa.nuevaLinea(l);
            JOptionPane.showMessageDialog(this,"Añadido con exito");
            limpiar();

        } else {

            JOptionPane.showMessageDialog(this, " la linea ya existe");

        }


    }//GEN-LAST:event_btnGrabarActionPerformed


