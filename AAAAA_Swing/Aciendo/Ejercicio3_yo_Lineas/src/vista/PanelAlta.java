    private void cargarParadas() {
        modeloPrimera.removeAllElements();
        modeloUltima.removeAllElements();
        modeloIntermedio.removeAllElements();

        ArrayList<Parada> paradas = miEmpresa.getParadas();
        for (int i = 0; i < paradas.size(); i++) {

            modeloPrimera.addElement(paradas.get(i));
            modeloUltima.addElement(paradas.get(i));
            modeloIntermedio.addElement(paradas.get(i));

        }
    }

    private void cargar() {

        modeloIntermedio.removeAllElements();

        ArrayList<Parada> paradas = miEmpresa.getParadas();
        for (int i = 0; i < paradas.size(); i++) {

            modeloIntermedio.addElement(paradas.get(i));

        }

    }

    private void limpiar() {

        this.txtCodigo.setText("");
        this.txtDenominacion.setText("");
        cmbPrimera.setSelectedIndex(0);
        cmbUltima.setSelectedIndex(0);
        this.txtFrecuencia.setText("");
        cargar();

    }
