    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        String cif, nombre, direccion, poblacion;
        
boolean resultado;
        
        if(!comprobarBlancos(txtCif) && !comprobarBlancos(txtNombre) && !comprobarBlancos(txtDireccion)
                && !comprobarBlancos(txtPoblacion)){
            
        cif=txtCif.getText();
        nombre= txtNombre.getText();
        direccion= txtDireccion.getText();
        poblacion= txtPoblacion.getText();
             
        resultado= miEmpresa.nuevoCliente(cif, nombre, direccion, poblacion);
        
        if(!resultado){
        
        
        JOptionPane.showMessageDialog(this, "NuevoCliente "+nombre, "Añadido con Exito", JOptionPane.INFORMATION_MESSAGE);
        limpiar();
        
        }else{
        
            JOptionPane.showMessageDialog(this, "El usurio Ya existe", "Alta", JOptionPane.ERROR_MESSAGE);
        
                txtCif.requestFocus();
                // selecciono el texto introducido
                txtCif.setSelectionStart(0);// comienzo de la selección
                txtCif.setSelectionEnd(txtCif.getText().length());// fin de la seleccion
        }
        
        
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    

    private void limpiar() {

        txtCif.setText("");
        txtNombre.setText("");
        txtDireccion.setText("");
        txtPoblacion.setText("");

    }

    public boolean comprobarBlancos(JTextField j) {

        boolean resultado;

        resultado = j.getText().trim().isEmpty();

        
        if (resultado) {

            JOptionPane.showMessageDialog(this, "Validacion", "Campo vacio" , JOptionPane.ERROR_MESSAGE);
            j.requestFocus();

        }

        return resultado;
    }

