


package ut6_ejer2_bidi;




public class Zona {
    
    String nombre;
    float sbase;

    public Zona() {
    }

    public Zona(String nombre, float sbase) {
        this.nombre = nombre;
        this.sbase = sbase;
    }

    public String getNombre() {
        return nombre;
    }

    public float getSbase() {
        return sbase;
    }
    
    
    
    
}
