



package ut6_ejer4;




public class Hijo {
    
    Fecha fnacimiento;
    boolean tiene;

    public Hijo(Fecha fnacimiento, boolean tiene) {
        this.fnacimiento = fnacimiento;
        this.tiene = tiene;
    }

    public Fecha getFnacimiento() {
        return fnacimiento;
    }

    public boolean isTiene() {
        return tiene;
    }
    
    
    
}
