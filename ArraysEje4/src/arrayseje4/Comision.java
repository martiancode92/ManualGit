
package arrayseje4;


public class Comision {
    
    float importe;
    float porcentaje;

    public Comision(float importe, float porcentaje) {
        this.importe = importe;
        this.porcentaje = porcentaje;
    }

    public float getImporte() {
        return importe;
    }

    public float getPorcentaje() {
        return porcentaje;
    }
    
    
}
