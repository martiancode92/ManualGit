    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        String login = txtLogin.getText();
        String pass = new String(pwdPass.getPassword());
        String nombre = txtNombre.getText();
        int resultado = miEmpresa.nuevoUsuario(login, pass, nombre);
        switch (resultado){
            case 0:
                JOptionPane.showMessageDialog(this, "usuario dado de alta", "Ventana informacion", JOptionPane.INFORMATION_MESSAGE);
                break;
            case -1:
                JOptionPane.showMessageDialog(this, "no se pueden dar de alta mas usuarios", "Ventana informacion", JOptionPane.ERROR_MESSAGE);
                break;
            case -2:
                JOptionPane.showMessageDialog(this, "existe un usuario con ese login", "Ventana informacion", JOptionPane.ERROR_MESSAGE);
        }
        if(resultado == 0){
            txtNombre.setText("");
            txtLogin.setText("");
            pwdPass.setText("");
        }
    }//GEN-LAST:event_btnGrabarActionPerformed


