
package ut6_ejercicio11;


public class Categoria {
    
    String Categoria;
    float sueldoBase;

    public Categoria(String Categoria, float sueldoBase) {
        this.Categoria = Categoria;
        this.sueldoBase = sueldoBase;
    }

    public String getCategoria() {
        return Categoria;
    }

    public float getSueldoBase() {
        return sueldoBase;
    }
    
    
    
}
