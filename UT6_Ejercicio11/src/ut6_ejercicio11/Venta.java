package ut6_ejercicio11;


public class Venta {
    
    float ventas;
    float comision;

    public Venta(float ventas, float comision) {
        this.ventas = ventas;
        this.comision = comision;
    }

    public float getVentas() {
        return ventas;
    }

    public float getComision() {
        return comision;
    }
    
    
    
}
