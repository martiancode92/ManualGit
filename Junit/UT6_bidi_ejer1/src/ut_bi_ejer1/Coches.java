
package ut_bi_ejer1;


public class Coches {
    
    String nombre;
    int tipo;

    public Coches() {
    }

    public Coches(String nombre, int tipo) {
        this.nombre = nombre;
        this.tipo = tipo;
    }

    public String getNombre() {
        return nombre;
    }

    public int getTipo() {
        return tipo;
    }
    
    
    
}
